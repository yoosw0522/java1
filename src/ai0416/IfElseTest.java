package ai0416;

public class IfElseTest {
    public static void main(String[] args) {
        int num = 200;

        if (num < 100) {
            System.out.println("조건식의 결과가 참이군요.");
            System.out.println("100보다 작군요.");
        } else {
            System.out.println("조건식의 결과가 거짓이군요.");
            System.out.println("100보다 크군요.");
        }

        System.out.println("프로그램 출력 끝!");
    }
}
